package ywrhee.week01; // Package 경로를 일치 시켜주어야 한다.

public class Errors // File 이름과 Class 이름을 일치시켜주어야 한다. (Class 이름의 시작은 대문자로 시작해야한다.)
{
  public static void main(String[] args) { // static은 소문자로 시작한다. main 함수의 Return Type이 지정되지 않았다. String은 대문자로 시작한다.
    System.out.println("202211342 이율원");
    System.out.println("이 프로그램은 많은 오류를 가지고 있다."); // 따옴표를 잘 보아야 한다.
    System.out.println("그러나 프로그램이 이 문장을 출력된다면"); // o와 0을 헷갈리지 않도록 하자. 실행문의 끝은 세미콜론으로 끝나야 한다.
    System.out.println("모든 오류를 고친 것이다."); // println은 소문자로 시작한다.
  }
}
